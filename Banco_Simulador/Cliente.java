package Banco_Simulador;

public class Cliente {

	private String nome;
	private int senha;

	public String getNome() {
		return nome;
	}

	public void setNome(String dados) {
		this.nome = dados;
	}

	public int getSenha() {
		return senha;
	}

	public void setSenha(int senha) {
		this.senha = senha;
	}

}
